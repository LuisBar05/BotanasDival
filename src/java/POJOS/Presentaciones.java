package POJOS;

/**
 *
 * @author Luis
 */
public class Presentaciones {

    private int idPresent, idUniMed;
    private float cantidad;
    private char status;

    public int getIdPresent() {
        return idPresent;
    }

    public void setIdPresent(int idPresent) {
        this.idPresent = idPresent;
    }

    public int getIdUniMed() {
        return idUniMed;
    }

    public void setIdUniMed(int idUniMed) {
        this.idUniMed = idUniMed;
    }

    public float getCantidad() {
        return cantidad;
    }

    public void setCantidad(float cantidad) {
        this.cantidad = cantidad;
    }

    public char getStatus() {
        return status;
    }

    public void setStatus(char status) {
        this.status = status;
    }

}
