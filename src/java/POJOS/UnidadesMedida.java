package POJOS;

/**
 *
 * @author Luis
 */
public class UnidadesMedida {

    private int idUniMed;
    private String descripcionUniMed;
    private char status;

    public int getIdUniMed() {
        return idUniMed;
    }

    public void setIdUniMed(int idUniMed) {
        this.idUniMed = idUniMed;
    }

    public String getDescripcionUniMed() {
        return descripcionUniMed;
    }

    public void setDescripcionUniMed(String descripcionUniMed) {
        this.descripcionUniMed = descripcionUniMed;
    }

    public char getStatus() {
        return status;
    }

    public void setStatus(char status) {
        this.status = status;
    }

}
